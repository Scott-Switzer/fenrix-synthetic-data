"""Tests for direct identifier scanner.

Validates that the scanner detects known forbidden patterns
and passes on clean content. All test identifiers are synthetic
or loaded from the professor review fixture.
"""

from __future__ import annotations

from pathlib import Path

import pytest
import yaml

from fenrix_synthetic.qa.direct_identifier_scan import ScanHit, ScanResult, scan_path, scan_zip_entries


FIXTURE_PATH = Path(__file__).resolve().parent.parent / "fixtures" / "professor_review" / "identification_cases.yaml"


@pytest.fixture(scope="module")
def source_data() -> dict[str, list[str]]:
    """Load company/ticker data from the professor review fixture."""
    with open(FIXTURE_PATH) as f:
        data = yaml.safe_load(f)
    sources = data["datasets"]["fenrix_alpha_v2"]["actual_sources"]
    company_names: list[str] = []
    tickers: list[str] = []
    for info in sources.values():
        company_names.append(info["company"])
        tickers.append(info["ticker"])
    return {"company_names": company_names, "tickers": tickers}


def _write_file(base: Path, name: str, content: str) -> Path:
    fp = base / name
    fp.write_text(content, encoding="utf-8")
    return fp


