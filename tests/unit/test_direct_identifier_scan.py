class TestDynamicPatterns:
    """Test that dynamic patterns (from config) are detected."""

    def test_detects_source_company_name(self, tmp_path: Path, source_data: dict[str, list[str]]) -> None:
        names = source_data["company_names"]
        if names:
            _write_file(tmp_path, "test.md", f"This company resembles {names[0]}.")
            result = scan_path(tmp_path, company_names=names)
            assert not result.passed
            assert any("company_name" in h.pattern_id for h in result.hits)

    def test_detects_source_ticker(self, tmp_path: Path, source_data: dict[str, list[str]]) -> None:
        tickers = source_data["tickers"]
        if tickers:
            _write_file(tmp_path, "test.md", f"The ticker {tickers[0]} is blocked.")
            result = scan_path(tmp_path, tickers=tickers)
            assert not result.passed
            assert any("ticker" in h.pattern_id for h in result.hits)


class TestScanResultProperties:
    """Test ScanResult properties."""

    def test_blocking_hits_filtered(self) -> None:
        hits = [
            ScanHit("a", 1, "test", "blocking", "preview"),
            ScanHit("b", 2, "test", "warning", "preview"),
        ]
        result = ScanResult(scanned_files=1, scanned_bytes=100, hits=hits, passed=False)
        assert len(result.blocking_hits) == 1
        assert len(result.warning_hits) == 1

    def test_to_dict(self) -> None:
        hits = [ScanHit("a", 1, "test", "blocking", "preview")]
        result = ScanResult(scanned_files=1, scanned_bytes=100, hits=hits, passed=False)
        d = result.to_dict()
        assert d["total_hits"] == 1
        assert d["blocking_hits"] == 1
        assert not d["passed"]


class TestZipEntryScan:
    """Test ZIP entry name scanning."""

    def test_detects_cik_in_zip_name(self, tmp_path: Path) -> None:
        import zipfile

        zip_path = tmp_path / "test.zip"
        with zipfile.ZipFile(zip_path, "w") as zf:
            zf.writestr("data/CIK_0000999999.txt", "content")
        hits = scan_zip_entries(zip_path)
        assert any("CIK" in h.pattern_id for h in hits)
